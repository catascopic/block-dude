function getLevels() {
	return LEVELS_ASCII.map(function(level) {
		let map = [];
		let row;
		let col;
		let dir;
		for (let i = 0; i < level.length; i++) {
			let line = level[i];
			let mapRow = [];
			for (let j = 0; j < line.length; j++) {
				let tile;
				switch (line.charAt(j)) {
				case ' ':
					tile = EMPTY;
					break;
				case '#':
					tile = WALL;
					break;
				case 'B':
					tile = BLOCK;
					break;
				case 'X':
					tile = GOAL;
					break;
				case '>':
				case '<':
					tile = EMPTY;
					row = i;
					col = j;
					dir = line.charAt(i) == '>' ? 1 : -1;
					break;
				default:
					throw 'unknown char' + line.charAt(i);
				}
				mapRow.push(tile);
			}
			map.push(mapRow);
		}
		return {
			map: map,
			row: row,
			col: col,
			dir: dir
		};
	});
}
